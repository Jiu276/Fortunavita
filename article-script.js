// Get article ID from URL
function getArticleId() {
    const urlParams = new URLSearchParams(window.location.search);
    return parseInt(urlParams.get('id')) || 1;
}

// Load article content
function loadArticle() {
    const articleId = getArticleId();
    const article = articleData[articleId];

    if (!article) {
        window.location.href = 'index.html';
        return;
    }

    // Update page elements
    document.getElementById('articleTitle').textContent = article.title;
    document.getElementById('articleCategory').textContent = article.category;
    document.getElementById('articleAuthor').textContent = article.author;
    document.getElementById('articleDate').textContent = formatDate(article.date);
    document.getElementById('articleImage').src = article.image;
    document.getElementById('articleImage').alt = article.title;
    document.getElementById('articleContent').innerHTML = article.content;

    // Update page title
    document.title = `${article.title} - Fortunavita Blog`;

    // Add tags
    const tagsContainer = document.getElementById('articleTags');
    tagsContainer.innerHTML = '<strong>Tags:</strong> ';
    article.tags.forEach(tag => {
        const tagSpan = document.createElement('span');
        tagSpan.textContent = tag;
        tagsContainer.appendChild(tagSpan);
    });

    // Load related articles
    loadRelatedArticles(article.relatedArticles);
}

// Load related articles
function loadRelatedArticles(relatedIds) {
    const relatedContainer = document.getElementById('relatedArticles');
    relatedContainer.innerHTML = '';

    relatedIds.forEach(id => {
        const related = articleData[id];
        if (related) {
            const card = document.createElement('div');
            card.className = 'related-card';
            card.innerHTML = `
                <img src="${related.image}" alt="${related.title}">
                <div class="related-card-content">
                    <h4><a href="article.html?id=${id}">${related.title}</a></h4>
                    <span>${formatDate(related.date)}</span>
                </div>
            `;
            relatedContainer.appendChild(card);
        }
    });
}

// Format date
function formatDate(dateString) {
    const options = { year: 'numeric', month: 'long', day: 'numeric' };
    return new Date(dateString).toLocaleDateString('en-US', options);
}

// Share functions
function shareOnFacebook() {
    const url = window.location.href;
    window.open(`https://www.facebook.com/sharer/sharer.php?u=${encodeURIComponent(url)}`, '_blank');
}

function shareOnTwitter() {
    const url = window.location.href;
    const title = document.getElementById('articleTitle').textContent;
    window.open(`https://twitter.com/intent/tweet?url=${encodeURIComponent(url)}&text=${encodeURIComponent(title)}`, '_blank');
}

function shareOnLinkedIn() {
    const url = window.location.href;
    window.open(`https://www.linkedin.com/sharing/share-offsite/?url=${encodeURIComponent(url)}`, '_blank');
}

